    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //Modificar Linea
        Linea aux=li;
        aux.setNombre(nombre.getText().toUpperCase());
        if(bd.updateLinea(aux)){
            s.info("Se ha actualizado la linea");
        }
        else{
            s.error("No se ha realizado la Actualizacion");
        }
        this.mostrar();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        //Nueva linea
        Linea aux=new Linea();
        String cad=s.opCadena("Escribe el nombre de la nueva linea:");
        if(s.valCadena(cad)){
            aux.setNombre(cad);
            aux.setIdLinea(0);
            if(bd.altaLinea(aux)){
                s.info("Se ha guardado la nueva linea");
            }
            else{
                s.error("Ha ocurrido un error");
            }
        }
        else{
            s.error("Se deben proporcionar los datos completos...");
        }
        this.mostrar();
    }//GEN-LAST:event_jButton3ActionPerformed

