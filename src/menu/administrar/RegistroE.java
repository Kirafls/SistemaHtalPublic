    private void taMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_taMouseClicked
        // TODO add your handling code here:
        //Es preferible
        int rowSelect=ta.getSelectedRow();
        //System.out.println(""+rowSelect);
        op.setId_empt(ta.getValueAt(rowSelect, 0).toString());
        op.setNum_emp(ta.getValueAt(rowSelect, 1).toString());
        op.setNombre(ta.getValueAt(rowSelect, 2).toString());
        op.setLinea(bd.lineaNum((String) ta.getValueAt(rowSelect, 3)));
        op.setTurno(bd.turnonum(ta.getValueAt(rowSelect, 4).toString()));
        op.setObservaciones(""+ta.getValueAt(rowSelect, 5));
        op.impOp();
        id_tarjeta.setText(op.getId_empt());
        num_operario.setText(op.getNum_emp());
        nombre.setText(op.getNombre());
        linea.setText((String) ta.getValueAt(rowSelect, 3));
        turno.setText(ta.getValueAt(rowSelect, 4).toString());
    }//GEN-LAST:event_taMouseClicked

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tab;
        String cadena=buscar.getText();
        if(s.valCadena(cadena)){
            tab=bd.buscarOp(cadena);
            ta.setModel(tab);
        }else{
            tab=bd.mostrarOp();
            ta.setModel(tab);
        }             
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //Registro de encargado
        if(op.validacion()){
            if(s.opcion("¬øRealmente quiere hacer Encargado a este operario?")){
                if(bd.altaEncargado(op)){
                    s.info("Inicia sesion con el nuevo encargado, recuerda cambiar la contrase√±a");
                    dispose();
                }                
            }
        }
        else{
            s.error("Seleccione un operador de la lista");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

