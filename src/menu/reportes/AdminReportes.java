    private void taMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_taMouseClicked
        // TODO add your handling code here:
        //Se genera el objeto registro en base a la tabla
        int row=ta.getSelectedRow();
        reg.setId_registro(Integer.parseInt(ta.getValueAt(row,0).toString()));
        id.setText(reg.getId_registro()+"");
        reg.setId_htal(ta.getValueAt(row, 1).toString());
        reg.setFecha_prestamo(ta.getValueAt(row, 2).toString());
        reg.setFecha_entrega(ta.getValueAt(row, 3).toString());
        reg.setNum_encargado(ta.getValueAt(row, 4).toString());
        reg.setNum_encargador(ta.getValueAt(row, 5).toString());
        reg.setNum_empleado(ta.getValueAt(row, 6).toString());
        resumen.setText(reg.impRegistroTep());
    }//GEN-LAST:event_taMouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        //Levantar Reporte
        Reporte rep=new Reporte();
        rep.setId(reg.getId_registro());
        rep.setOperario(reg.getNum_empleado());
        rep.setEncargado(encargado.getNumEmpleado());
        rep.setHerramental(reg.getId_htal());
        AltaReporte f=new AltaReporte(this,rep);
        this.setEnabled(false);
        f.setVisible(true);
    }//GEN-LAST:event_jButton3ActionPerformed

