    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        //Cerrar Reporte
        String sol=s.opCadena("Escribe la solucion que se le dio al reporte ");
        if(s.valCadena(sol)&&reporte.valReporte()){
            reporte.setSolucion(sol);
            reporte.setFechaCierre(bd.fechaActual());
            if(bda.bajaReporte(reporte)){
                s.info("Se ha cerrado el reporte");
            }
            else{
                s.error("Ha ocurrido un error...");
            }
        }
        else{
            s.error("Completa los datos");
        }
        id.setText("");
        resumen.setText("");
        mostrar();        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void taMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_taMouseClicked
        // TODO add your handling code here:
        //Se genera el objeto registro en base a la tabla
        int row=ta.getSelectedRow();
        reporte.setId(Integer.parseInt(ta.getValueAt(row, 0).toString()));
        reporte.setEncargado(ta.getValueAt(row, 1).toString());
        reporte.setHerramental(ta.getValueAt(row, 2).toString());
        reporte.setOperario(ta.getValueAt(row, 3).toString());
        reporte.setTipoReporte(bd.tipoCuenta(ta.getValueAt(row, 4).toString()));
        reporte.setProblematica(ta.getValueAt(row, 5).toString());
        reporte.setFechaAbierto(ta.getValueAt(row, 6).toString());
        
        id.setText(reporte.getId()+"");
        resumen.setText(reporte.impReporte());
        
    }//GEN-LAST:event_taMouseClicked

