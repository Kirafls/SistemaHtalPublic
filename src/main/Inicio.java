    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        //Validacion de contrase単as, ademas de la entrada a las bases de datos
        //Fase final, de momento solo se realizara la validacion de una contrase単a 
        String contrasena=new String(contra.getPassword());
        String numeroEmpleado=numEmpleado.getText();
        if(sistema.valCadena(numeroEmpleado)){
            if(sistema.valCadena(contrasena)){
                System.out.println("Las cadenas tienen texto... ");
                //Code here, primera validacion
                encargado= bd.validarSesion(numeroEmpleado,contrasena);
                if(encargado==null){
                    //sistema.error("Los datos proporcionados no son correctos");
                    contra.setText("");
                    numEmpleado.setText("");
                }else{
                    E_Herramental f=new E_Herramental(encargado);
                    f.setVisible(true);
                    sistema.info("Bienvenido  "+encargado.getNombre());
                    dispose();                    
                }
            }
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

